#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	size_t	i;

	i = ft_strlen(s);
	while (i > 0)
	{
		if (s[i] == (char) c)
			return ((char *) &s[i]);
		i--;
	}
	if (s[0] == (char) c)
		return ((char *)s);
	if (c == '\0')
		return ((char *) &s[ft_strlen(s)]);
	return (NULL);
}
